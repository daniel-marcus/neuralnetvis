import { useMemo } from "react"
import figlet from "figlet/lib/figlet"
import type { Fonts } from "figlet"

const MAX_CHARS_PER_LINE = 10

figlet.parseFont("Blur Vision ASCII", blurVision())

interface AsciiTextProps {
  children: string
  className?: string
}

export function AsciiText({ children, className }: AsciiTextProps) {
  const ascii = useAsciiText(children)
  return <pre className={className}>{ascii}</pre>
}

const useAsciiText = (input: string) => {
  return useMemo(() => {
    const sanitizedInput = replaceUmlauts(input)
    const textWithLineBreaks = splitWithThreshold(sanitizedInput).join("\n")
    return figlet
      .textSync(textWithLineBreaks, {
        font: "Blur Vision ASCII" as Fonts,
      })
      .replace(/[░▒]/g, " ")
      .replace(/^.{2}/gm, "")
  }, [input])
}

function replaceUmlauts(str: string) {
  return str
    .replace(/Ä/g, "Ae")
    .replace(/Ö/g, "Oe")
    .replace(/Ü/g, "Ue")
    .replace(/ä/g, "ae")
    .replace(/ö/g, "oe")
    .replace(/ü/g, "ue")
    .replace(/ß/g, "ss")
}

export function splitWithThreshold(
  str: string,
  threshold = MAX_CHARS_PER_LINE
) {
  return str.split(" ").reduce((acc, word) => {
    const lastChunk = acc.at(-1) || ""
    const newChunk = lastChunk ? `${lastChunk} ${word}` : word
    const length = newChunk.replace(/(.*)\n/, "").length
    return length <= threshold
      ? [...acc.slice(0, -1), newChunk]
      : [...acc, word]
  }, [] as string[])
}

